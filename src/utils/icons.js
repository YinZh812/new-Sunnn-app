/**
 * 渲染一个 Lucide SVG 字符串。
 * @param {string} name           图标名（必须存在于 LUCIDE_PATHS）
 * @param {number} [size=20]      宽高（px）
 * @param {number} [strokeWidth=1.6]
 * @returns {string} SVG HTML 字符串；name 无效时返回空串
 */
export function lucideSvg(name, size = 20, strokeWidth = 1.6) {
  const body = LUCIDE_PATHS[name];
  if (!body) return "";
  return `<svg width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="${strokeWidth}" stroke-linecap="round" stroke-linejoin="round">${body}</svg>`;
}

/**
 * 通用图标渲染：
 *   - 输入 "lucide:car" → 渲染对应 Lucide SVG
 *   - 输入 "🍜" 或任何 emoji → 直接返回原值
 *   - 空值返回空串
 */
export function renderIcon(value, size = 18, strokeWidth = 1.6) {
  if (!value) return "";
  if (typeof value === "string" && value.startsWith("lucide:")) {
    return lucideSvg(value.slice(7), size, strokeWidth);
  }
  return value;
}

